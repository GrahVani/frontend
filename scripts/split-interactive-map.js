/**
 * Split the monolithic interactive-map.ts (1,077 entries) into per-module
 * _map.ts files (~25 entries each) and generate a module-registry.ts that
 * loads only the needed module's map at runtime.
 *
 * This reduces lesson page cold-compile from ~2.5min to ~10-15s because
 * webpack only processes the ~25 imports for the specific module instead of
 * all 1,077.
 */

const fs = require("fs");
const path = require("path");

const INTERACTIVE_DIR = path.resolve(
  __dirname,
  "../src/components/learning-runtime/interactive"
);
const MAP_FILE = path.join(INTERACTIVE_DIR, "interactive-map.ts");

// 1. Parse the existing interactive-map.ts
const content = fs.readFileSync(MAP_FILE, "utf-8");
const entryRegex =
  /"([^"]+)":\s*\(\)\s*=>\s*import\("([^"]+)"\)/g;

const entries = [];
let match;
while ((match = entryRegex.exec(content)) !== null) {
  entries.push({ key: match[1], importPath: match[2] });
}

console.log(`Parsed ${entries.length} entries from interactive-map.ts`);

// 2. Group entries by tier-module
const groups = {};
for (const entry of entries) {
  // key format: "tier-1/module-1/component-name"
  const parts = entry.key.split("/");
  if (parts.length < 3) {
    console.warn(`Skipping malformed key: ${entry.key}`);
    continue;
  }
  const tierModule = `${parts[0]}/${parts[1]}`;
  if (!groups[tierModule]) groups[tierModule] = [];
  groups[tierModule].push({
    componentKey: parts.slice(2).join("/"),
    fullKey: entry.key,
    importPath: entry.importPath,
  });
}

console.log(
  `Grouped into ${Object.keys(groups).length} tier-module combinations`
);

// 3. Generate per-module _map.ts files
for (const [tierModule, moduleEntries] of Object.entries(groups)) {
  const [tier, mod] = tierModule.split("/");
  const mapDir = path.join(INTERACTIVE_DIR, tier, mod);

  if (!fs.existsSync(mapDir)) {
    console.warn(`Directory not found, skipping: ${mapDir}`);
    continue;
  }

  const lines = [
    "// AUTO-GENERATED by scripts/split-interactive-map.js — do not edit.",
    "export const map: Record<string, () => Promise<any>> = {",
  ];

  for (const entry of moduleEntries) {
    // Use relative imports from the _map.ts location
    lines.push(
      `  "${entry.componentKey}": () => import("./${entry.componentKey}/index"),`
    );
  }

  lines.push("};", "");

  fs.writeFileSync(path.join(mapDir, "_map.ts"), lines.join("\n"), "utf-8");
  console.log(
    `  ${tierModule}/_map.ts — ${moduleEntries.length} entries`
  );
}

// 4. Generate module-registry.ts with EXPLICIT imports (no template literals!)
const registryLines = [
  "// AUTO-GENERATED by scripts/split-interactive-map.js — do not edit.",
  "//",
  "// Each entry lazily loads a per-module map (~25 entries) instead of the",
  "// monolithic 1,077-entry interactive-map. This reduces lesson page",
  "// cold-compile from ~2.5min to ~10-15s.",
  "",
  "type ModuleMap = Record<string, () => Promise<any>>;",
  "",
  "const registry: Record<string, () => Promise<{ map: ModuleMap }>> = {",
];

const sortedKeys = Object.keys(groups).sort();
for (const tierModule of sortedKeys) {
  const [tier, mod] = tierModule.split("/");
  registryLines.push(
    `  "${tierModule}": () => import("./${tier}/${mod}/_map"),`
  );
}

registryLines.push("};", "");
registryLines.push(
  "/**",
  " * Load a single interactive component by tier-module key and component slug.",
  " * Only the ~25 imports for the specific module are processed by webpack,",
  " * not all 1,077.",
  " */",
  "export async function loadInteractive(",
  "  tierModule: string,",
  "  componentKey: string",
  "): Promise<any | null> {",
  "  const mapLoader = registry[tierModule];",
  "  if (!mapLoader) return null;",
  "  const { map } = await mapLoader();",
  "  const componentLoader = map[componentKey];",
  "  if (!componentLoader) return null;",
  "  try {",
  "    return await componentLoader();",
  "  } catch (err) {",
  '    console.error(`Failed to load interactive ${tierModule}/${componentKey}`, err);',
  "    return null;",
  "  }",
  "}",
  ""
);

fs.writeFileSync(
  path.join(INTERACTIVE_DIR, "module-registry.ts"),
  registryLines.join("\n"),
  "utf-8"
);

console.log(
  `\nGenerated module-registry.ts with ${sortedKeys.length} module loaders`
);
console.log("Done!");
